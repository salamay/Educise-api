package com.school.webapp.RequestModel;

public class ClassModel {
    private String id;
    private String classes;

    public ClassModel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getClasses() {
        return classes;
    }

    public void setClasses(String classes) {
        this.classes = classes;
    }
}
